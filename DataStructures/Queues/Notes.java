public class Notes {
    
    /*
     * Queue:
     * A Queue is a linear data structure that follows the First In First Out (FIFO) principle.
     * Elements are added at the rear (enqueue) and removed from the front (dequeue).
     * 
     * Common Operations:
     * 1. enqueue(element): Adds an element to the rear of the queue.
     * 2. dequeue(): Removes and returns the front element of the queue.
     * 3. peek(): Returns the front element without removing it.
     * 4. isEmpty(): Checks if the queue is empty.
     * 5. size(): Returns the number of elements in the queue.
     * 
     * Implementations:
     * - Array-based Queue
     * - Linked List-based Queue
     * - Circular Queue
     * 
     * Use Cases:
     * - Task scheduling
     * - Handling requests in web servers
     * - Breadth-First Search (BFS) in graph algorithms
    */

    /* Additional notes:
        * - Queues can be implemented using various data structures like arrays or linked lists.
        * - Circular queues help in optimizing space by reusing empty slots.
        * - Priority Queues are a special type of queue where elements are dequeued based on priority rather than order of insertion.
    */
    

}
